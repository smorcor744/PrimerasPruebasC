namespace PrimerasPruebasC;

public class Minion
{
    public int MaxLife;
    public int Damage;
    public int Armor;
    public int Life;

    public Minion(int maxLife, int damage, int armor, int life)
    {
        MaxLife = maxLife;
        Damage = damage;
        Armor = armor;
        Life = life;
    }
}
