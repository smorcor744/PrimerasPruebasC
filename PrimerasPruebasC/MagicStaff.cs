public class MagicStaff : Item
{
    public string Name;
    private int Damage = 5;
    
    public abstract void Apply(Character character)
    {
        character.BaseDamage += Damage;

        public void HabilityGenerate(int mana)
                {
                    if ( MaxManaPoints >= mana )
                    {
                    public int percentage = mana * 0.01
                    Minion minion = new Minion( int MaxLife,int Damage , int Armor, int Life)
                    {
                        MaxLife = character.MaxHitPoints * percentage,
                        Damage = character.TotalDamage * percentage,
                        Armor = character.TotalArmor * percentage,
                        Life = .MaxLife
                    }
                    return minion
                    }
                }
    }
    public abstract void Desply(Character character);

}